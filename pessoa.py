class Pessoa:
    def __init__(self, nome: str, data_nascimento: str, endereco: str, cpf: str, estado_civil: str):
        self.nome = nome
        self.data_nascimento = data_nascimento
        self.endereco = endereco
        self.cpf = cpf
        self.estado_civil = estado_civil

@property

def nome(self):
    return self._nome

@nome.setter
def nome(self):
    return self._nome


@property

def data_nascimento(self):
    return self._data_nascimento

@data_nascimento.setter
def data_nascimento(self):
    return self._data_nascimento

@property

def endereco(self):
    return self._endereco

@endereco.setter
def endereco(self):
    return self._endereco

@property

def cpf(self):
    return self._cpf
@cpf.setter
def cpf(self):
    return self._cpf

@property

def estado_civil(self):
    return self._estado_civil

@estado_civil.setter
def estado_civil(self):
    return self._estado_civil
